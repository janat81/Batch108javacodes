package day10loops;

public class WhileLoop01 {

    public static void main(String[] args) {

        // Type code to print integers from 3 to 6
        // 1.Way: Use for-loop

        for(int i=3; i<7; i++){
            System.out.println(i + " ");
        }


        //2.Way: while-loop
        int i=3;
        while(i<7){
            System.out.println(i+" ");
            i++;
        }
        //Example2:
    }


}
